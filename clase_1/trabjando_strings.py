saludo = "hoLa muNdo"
# pasar  a minuscula.
print(saludo.lower())
# pasar a mayuscula
print(saludo.upper())
# primera letra en mayuscula
print(saludo.capitalize())
# número de caracteres
print(len(saludo))
# reemplazar palabra
print(saludo.replace("muNdo","Word"))
#count( ) : retorna el número de veces que se repite un conjunto de caracteres especificado
print(saludo.count("muNdo"))