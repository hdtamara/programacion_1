frase = input("Digita una frase: ")
#Convertir la frase en mayuscula.
print(frase.upper())
# Convertir la frase en minuscula
print(frase.lower())
# contar la longitud de la caneda
print(len(frase))
# Cambiar todas las a por i
print(frase.replace("a","i"))
# Contar cuantas veces se repite la letra a
print(frase.count("a"))
#ver si se encuentra la palabra Mundo
print(frase.find("Mundo"))